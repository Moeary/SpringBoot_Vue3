CREATE TABLE medical_technology (
                                    id INT(9) NOT NULL AUTO_INCREMENT PRIMARY KEY,
                                    tech_code VARCHAR(64),
                                    tech_name VARCHAR(64),
                                    tech_format VARCHAR(64),
                                    tech_price DECIMAL(8,2),
                                    tech_type VARCHAR(64),
                                    price_type VARCHAR(64),
                                    deptment_id INT(9),
                                    FOREIGN KEY (deptment_id) REFERENCES department(id)
);

